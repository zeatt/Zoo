package primitive;

public class IntegerTest {
    int a;
    int b;

    public IntegerTest(int a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "IntegerTest{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
